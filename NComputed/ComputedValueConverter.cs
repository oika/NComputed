using System;
using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Data;

namespace NComputed
{
    /// <summary>
    /// An converter to convert Computed object to that value.
    /// </summary>
    public class ComputedValueConverter : IValueConverter
    {
        /// <summary>
        /// Converts Computed object to that value.
        /// </summary>
        /// <param name="value"></param>
        /// <param name="targetType"></param>
        /// <param name="parameter"></param>
        /// <param name="culture"></param>
        /// <returns></returns>
        public object Convert(object value, Type targetType, object parameter, CultureInfo culture)
        {
            var computed = value as ComputedBase;
            if (computed == null) return value;

            return computed.GetValue();
        }

